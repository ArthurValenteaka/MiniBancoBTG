    private void pesquisar_cliente() {
        String sql = "select idcli as Id, nomecli as Nome, fonecli as fone from tbclientes where nomecli like ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtPesquisa.getText() + "%");
            rs = pst.executeQuery();
            tblClientes.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    private void setar_campos() {
        int setar = tblClientes.getSelectedRow();
        txtCli.setText(tblClientes.getModel().getValueAt(setar, 0).toString());

    }

    private void realizar_deposito() {
        String sql = "insert into tbbanco (valor_saque,valor_deposito,idcli) values (?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);

            pst.setString(1, txtValSaida.getText());
            pst.setString(2, ("0"));
            pst.setString(3, txtCli.getText());

            if ((txtCli.getText().isEmpty()) || (txtValSaida.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatorios");
            } else {
                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Sucesso!");
                    txtCli.setText(null);
                    txtValSaida.setText(null);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

